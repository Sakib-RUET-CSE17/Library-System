    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("") || jTextField5.getText().equals("") || jTextField6.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Field Must Not Be Empty!");

        } else {
            String B_ID, name, edition, publisher, price, pages;
            B_ID = jTextField1.getText();
            name = jTextField2.getText();
            edition = jTextField3.getText();
            publisher = jTextField4.getText();
            price = jTextField5.getText();
            pages = jTextField6.getText();
            try {
                String query = "update book set Name='" + name + "',Edition='" + edition + "',Publisher='" + publisher + "',Price='" + price + "',Pages='" + pages + "' where Book_ID='" + B_ID + "' ";
                pst = con.prepareStatement(query);
                pst.execute();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                table_data();
                JOptionPane.showMessageDialog(null, "Successfully Updated!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

